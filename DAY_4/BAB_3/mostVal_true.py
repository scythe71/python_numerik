# Kebanyakan nilai di Python dianggap benar (True), kecuali beberapa nilai yang dianggap salah (False).
bool("abc")  # True
bool(123)  # True
bool(["apple", "cherry", "banana"])  # True

# Nilai-nilai yang dianggap salah (False) meliputi:
bool(False)  # False
bool(None)  # False
bool(0)  # False
bool("")  # False
bool(())  # False
bool([])  # False
bool({})  # False

# Bool adalah tipe data di Python yang dapat memiliki dua nilai: True atau False.
# Nilai True dan False adalah instance dari tipe data bool.